#이미지 분류 모델 구축하고 이미지 분석

#데이터 집합 선택: 학생들은 CIFAR-10 또는 ImageNet과 같은 물체나 동물의 이미지가 있는 데이터 세트를 선택할 수 있다.

#데이터 전처리: 이미지 크기 조정, 교육 및 테스트 세트로 분할, 픽셀 값 정규화를 통해 데이터를 정리하고 준비합니다.

#딥 러닝 모델 구축: CNN(convolutional neural network)을 사용하여 특징에 따라 이미지를 분류한다. 
#간단한 아키텍처로 시작한 다음 더 많은 레이어를 추가하거나 필터 수를 늘리거나 활성화 기능을 변경하여 개선합니다.

#모델 교육: 교육 세트를 사용하여 모델을 교육하고 과적합을 방지하기 위해 테스트 세트의 성능을 모니터링합니다.

#모형 평가: 테스트 세트에서 모델의 성능을 평가하고 기준 정확도와 비교합니다.

#모델 미세 조정: 하이퍼 파라미터를 조정하거나 다른 아키텍처를 사용하여 모델의 성능을 개선하여 모델을 미세 조정합니다.
